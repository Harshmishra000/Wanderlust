const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const Review = require("./review.js");

const listingSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  description: String,
  
  image: {
    filename: { type: String },
    url: {
      type: String,
      default: "https://images.unsplash.com/photo-1551524164-687a55dd1126?...",
      set: v => v === "" ? "default-url" : v
    }
  },

  price: Number,
  location: String,
  country: String,

  category: {
    type: String,
    enum: [
    "trending",
    "rooms",
    "iconic cities",
    "mountain",
    "castle",
    "amazing pools",
    "camping",
    "farms",
    "arctic",
    "dome",
    "boat"
]
,
    required: true
  },

  reviews: [
    {
      type: Schema.Types.ObjectId,
      ref: "Review"
    }
  ],

  Owner: {
    type: Schema.Types.ObjectId,
    ref: "User",
  },

  geometry: {
    type: {
      type: String,
      enum: ["Point"],
      required: true
    },
    coordinates: {
      type: [Number],
      required: true,
    }
  },
});

listingSchema.post("findOneAndDelete", async (listing) => {
  if (listing) {
    await Review.deleteMany({ _id: { $in: listing.reviews } });
  }
});

const Listing = mongoose.model("Listing", listingSchema);
module.exports = Listing;
